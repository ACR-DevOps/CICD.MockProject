using System;

namespace Class.Lib.One
{
    public class MathOperation
    {
        public int Add(int firstNum, int secondNum)
        {
            int result = firstNum + secondNum;
            return result;
        }
        public int Multiply(int firstNum, int secondNum)
        {
            int result = firstNum * secondNum;
            return result;
        }
    }
}
